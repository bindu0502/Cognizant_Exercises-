class Dtype {
    public static void main(String args[]){
        int myint = 87;
        double int_to_double=myint;//implicit typecasting
        System.out.println("Integer value : "+myint);
        System.out.println("Int to double value : "+int_to_double);
        double mydouble=56.8976;
        int double_to_int=(int)mydouble;//Explict Typecasting
        System.out.println("Double Value : "+mydouble);
        System.out.println("double to int : "+double_to_int);
    }
    
}
